void Factorial(int iNo)                     
{                                           
    int iCnt = 0;                           
    for (iCnt = 2; iCnt <= (iNo/2); iCnt++)   //O(n/2)  
    {                                       
        if ((iNo % iCnt) == 0)              
        {                                   
            printf("%d\t", iCnt);           
        }                                   
    }                                       
}                                          

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Entery point function
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("Enter the number :");
    scanf("%d", &iValue);

    Factorial(iValue);

    return 0;
}


